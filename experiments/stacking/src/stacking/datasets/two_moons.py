"""Two-moons loader: small 2-D synthetic classification dataset."""

from __future__ import annotations

from typing import Any

import numpy as np
from sklearn.datasets import make_moons
from sklearn.model_selection import train_test_split

from stacking.data import Dataset


def load(
    *,
    n_samples: int = 2000,
    noise: float = 0.2,
    calib_frac: float = 0.2,
    test_frac: float = 0.2,
    seed: int = 0,
    **_: Any,  # noqa: ANN401
) -> Dataset:
    """Build a two-moons dataset with train / calib / test splits.

    Args:
        n_samples: Total number of samples generated by sklearn's
            ``make_moons``.
        noise: Gaussian noise standard deviation passed to ``make_moons``.
        calib_frac: Fraction of the train+calib pool reserved for
            calibration. Applied after the test split is carved off.
        test_frac: Fraction of the full dataset reserved for testing.
        seed: Random seed for both ``make_moons`` and the splits.
        **_: Loader-agnostic kwargs forwarded by
            :func:`stacking.data.load_dataset` (e.g. ``encoder=``) are
            accepted and ignored.

    Returns:
        A :class:`stacking.data.Dataset` with ``in_features=2``,
        ``num_classes=2``, and ``meta={"name": "two_moons", "seed": seed}``.
    """
    X, y = make_moons(n_samples=n_samples, noise=noise, random_state=seed)
    X = X.astype(np.float32)
    y = y.astype(np.int64)

    X_trainval, X_test, y_trainval, y_test = train_test_split(
        X, y, test_size=test_frac, stratify=y, random_state=seed,
    )
    X_train, X_calib, y_train, y_calib = train_test_split(
        X_trainval, y_trainval,
        test_size=calib_frac,
        stratify=y_trainval,
        random_state=seed,
    )
    return Dataset(
        X_train=X_train,
        y_train=y_train,
        X_calib=X_calib,
        y_calib=y_calib,
        X_test=X_test,
        y_test=y_test,
        in_features=2,
        num_classes=2,
        meta={"name": "two_moons", "seed": seed},
    )
